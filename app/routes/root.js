//====HANDLE ROOT ROUTES =============
var router = require('express').Router();

//====테스팅 용도 =============================
router.get('/', function (req, res, next) {
    res.send("환영합니다");
});

// 200: 요청이 성공적
// 201: 성공적으로 생성되었음

// 400: 잘못된 문법
// 401: 비인증 // jwt 잘못되어도 뜸
// 403: 권한부족
// 404: NOT FOUND
// 409: 리소스와 타켓리소스가 충돌

// 500: 서버에러

// 유저못찾으면 401
// 요청성공 200
// 새로 생성된거면 201
// 수정삭제 본인아니면 403
// 게시물이나 유저 조회안되면 404


module.exports = router;